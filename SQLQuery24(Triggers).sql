/**************DML Triggers ****************/
/*
In SQL Server there are 3 types of triggers
1. DML Triggers
2. DDL Triggers
3. Logon Triggers

DML Triggers are fired automatically in response to DML events (INSERT,UPDATE & DELETE)

DML Triggers can be againn classified into 2 types
1. After triggers (Sometimes called as FORtriggers)
2. Instead of Triggers

After triggers, fires after the trigger action.
The INSERT, UPDATE and DELETE
statements causes an after trigger to fire after the respective statemnets complete execution.

INSTEAD of Triggers, fires instead of the triggering action.
The INSERT,UPDATE and DELETE statements, causes an INSTEAD OF trigger to fire INSTEAD OF the respective
statement execution.
